public class Main {
    public static void main(String[] args) {
        int x = 5;
        boolean isXGreater;

        //Operador de comparação
        //Tem 6 tipos de operadores
        //> - maior
        //< - menor
        //>= - maior ou igual
        //<= - menor ou igual
        //== - igual
        //!= - diferente
        if(x > 1){
            isXGreater = true;
        } else{
            isXGreater = false;
        }

        System.out.println(isXGreater);
    }
}