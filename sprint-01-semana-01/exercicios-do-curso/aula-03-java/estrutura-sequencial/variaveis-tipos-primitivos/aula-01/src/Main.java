public class Main {
    public static void main(String[] args) {
        //1 bit = 2
        //8 bits = 2x2x2x2x2x2x2x2 = 256, que seria a quantidade que o byte consegue armazenar

        //Armazena entre -128 e 127 (8 bits)
        byte a = 1;

        //Armazena entre -32768 e 32767 (16 bits)
        short i = 1;

        //Armazena muito mais (32 bits)
        int idade = 2516;

        //ARMAZENA MUITO MAIS (64 bits)
        long dados = 25382782;

        //Consegue armazenar menos que o double, e normalmente não é muito usado (32 bits)
        float peso = 100.11F;

        //(64 bits)
        double altura = 1.68;

        //Armazena apenas um caractere (16 bits)
        char sexo = 'M';

        //Os valores possiveis são true e false (1 bit)
        boolean feliz = true;

        //E tem a string tambem

    }
}